                <h3>GUIDELINES TO AUTHORS</h3>
                <br>
                <p>Below are some useful instructions to assist the authors to submit their papers for ICWITE 2022:</p>
                <br>
                <h4>ORIGINALITY OF SUBMITTED PAPERS:</h4>
                <br>
                <p>Papers submitted to ICWITE 2022 shall contain original work by the author(s) that have not been 
                    published or submitted for publication elsewhere. The IEEE anti- plagiarism policy is applicable 
                    to all submissions. The author(s) are solely responsible for any plagiarized submission.</p>
                <h4>LONG ABSTRACT SUBMISSION:</h4>
                <br>
                <p>
                    ICWITE invites researchers and scholars to submit their paper/ poster abstracts for selection at the conference.<br><br>
                    Long Abstracts should include in no more than a 1000 words, <br>
                    1) the overall purpose of the study and the research problem(s) you investigated; <br>
                    2) the basic design of the study; <br>
                    3) major findings or trends found as a result of your analysis; and,<br>
                    4) a brief summary of your interpretations and conclusions.<br><br>
                    <b>NOTE:</b> Once the abstract is submitted, we shall start reviewing it, so authors cannot update/resubmit the same. <br><br>
                    Upon review and selection for presentation at the conference, scholars are expected to submit their research papers.<br><br>
                    All papers are required to be prepared and submitted in the IEEE Standard two - column conference format
                    of A4 size in English. Papers must be four to six pages in length, including texts, Figures, photographs
                    and references. Any submitted paper that exceeds six pages will be rejected. The first page must include 
                    the title of the paper, author(s), affiliations, address and text. Please do not include page numbers 
                    on submitted documents. Failing to conform to the standard format will result in rejection. Detailed 
                    format information is available in IEEE Website.<br><br>
                    The authors are required to use the standard IEEE manuscript template for conferences. The Microsoft 
                    Word template in can be downloaded from IEEE conference template page: <a style="color: black;" href="https://www.ieee.org/conferences/publishing/templates.html"> Click Here</a><br><br>
                    The paper shall consist of original and previously unpublished work, including specific results. The 'Introduction' of the paper shall clearly indicate the unique aspects of the submission and how it is related to the previous work.<br><br>
                    All paper submissions must be electronic, using IEEE Xplore compliant PDF format (*.pdf). Failure to do so may result in the rejection of the paper.<br><br>
                    Type 3 fonts (bitmaps) will not be accepted. Authors can use the IEEE PDF eXpress to generate compliant PDF Files for final submission.

                </p>
                <h4>PAPER & POSTER UPLOAD:</h4>
                <br>
                <p>
                    All papers must be submitted in PDF or as a Microsoft Word Document via Microsoft Conference Management Toolkit: <a style="color:darkblue;" href="https://cmt3.research.microsoft.com/ICWITE2022/">CMT</a>
                    <br><br>
                    <b>(Note: Authors please clear your browser cache and login to CMT before uploading your papers.)</b>
                    <br><br>
                    The submitting authors may be required to create a Microsoft Conference Management Toolkit account before uploading the paper (if the submitting author does not have a Microsoft Conference Management Toolkit account). 
                    <br><br>
                    While uploading the paper, the authors should provide the title of the paper, a long abstract of no more than 1000 words and keywords in the respective text boxes in the paper submission page.
                    <br><br>
                    The long abstracts of all accepted & registered papers will be available on IEEE Xplore. Camera ready papers that meet the quality standards of IEEE will be included in IEEE Xplore. 
                </p>
                <h4>TYPES OF PRESENTATION AT THE CONFERENCE:</h4>
                <br>
                <p>
                Two types of presentation can be opted for by the authors:
                    <br><br>
                        1. oral presentation (presented in one of track sessions)<br>
                        2. poster presentation (presented in one of poster sessions)<br>
                    <br><br>
                    Papers with archival value (archival papers), meaning those that contribute significantly to the state of the art and report final results of research, with proper literature review, will be thoroughly reviewed and may be candidates for publication consideration in special issues or journals.
                </p>

